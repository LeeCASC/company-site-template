// 获取新闻预览文本（标题 + 前两行正文 + 日期）
export function getNewsPreview(id: string, locale: 'zh' | 'en' = 'zh'): { title: string; preview: string; date: string } {
  const news = newsData[id] || newsData['1'];
  const content = news.content[locale];
  const title = news.title[locale];
  
  // 提取日期 - 支持中英文格式
  let date = '';
  const dateMatch = content.match(/日期[：:]\s*(.+)|Date[：:]\s*(.+)/);
  if (dateMatch) {
    date = (dateMatch[1] || dateMatch[2] || '').trim();
  }
  
  // 移除日期行（如果存在），然后获取前两行正文
  const lines = content.split('\n').filter(line => {
    const trimmed = line.trim();
    return trimmed !== '' && !trimmed.match(/^(日期|Date)[：:]/);
  });
  
  // 移除HTML标签，只保留纯文本用于预览
  const removeHtmlTags = (text: string) => {
    return text.replace(/<[^>]*>/g, '').trim();
  };
  
  const firstTwoLines = lines.slice(0, 2).map(line => removeHtmlTags(line.trim())).join(' ');
  const previewText = firstTwoLines.length > 0 ? firstTwoLines : removeHtmlTags(content.substring(0, 100));
  
  return {
    title: title,
    preview: previewText,
    date: date
  };
}

